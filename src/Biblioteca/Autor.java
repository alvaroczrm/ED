package Biblioteca;

public class Autor 
{
	private String nombre;
	private String apellidos;
	private String nacionalidad;
	
	public Autor() 
	{
		nombre = "";
		apellidos = "";
		nacionalidad = "";
	}
	public Autor (String n, String a, String na) 
	{
		nombre = n;
		apellidos = a;
		nacionalidad = na;
	}

}
